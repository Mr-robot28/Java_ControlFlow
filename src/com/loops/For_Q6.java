package com.loops;

public class For_Q6 {

	public static void main(String[] args) {
		
		System.out.println("Q6: Print the table of any number (e.g., n = 7).");
		System.out.println();

		int n = 8;

		for (int i = 1; i <= 10; i++) {

			System.out.println((n) + " ✖️ " + i + " = " + (n * i));

		}
	}
}
