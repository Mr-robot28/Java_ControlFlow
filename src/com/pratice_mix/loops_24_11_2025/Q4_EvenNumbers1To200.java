package com.pratice_mix.loops_24_11_2025;

public class Q4_EvenNumbers1To200 {

	public static void main(String[] args) {

		System.out.println("Q4) Print all even numbers between 1 to 200 using for loop:\n");
		for (int i = 1; i <= 200; i++) {
			if (i % 2 == 0)
				System.out.println(i);
		}
	}

}
