package com.pratice_mix.switch_case_pratice_21_11_2025;

public class Q4_Vowels {
	
public static void main(String[] args) {
		
		System.out.println("Q4. Accept a character and check whether it is a vowel or consonant using switch case.");
		System.out.println();
		
		char alph = 'd';

		switch (alph) {

		case 'a':
			System.out.println(alph + " is an Vowel");
			break;
		case 'e':
			System.out.println(alph + " is an Vowel");
			break;
		case 'i':
			System.out.println(alph + " is an Vowel");
			break;
		case 'o':
			System.out.println(alph + " is an Vowel");
			break;
		case 'u':
			System.out.println(alph + " is an Vowel");
			break;
		default:
			System.out.println(alph + " is an Consonant ...! ");
		}
	}
	
	

}
